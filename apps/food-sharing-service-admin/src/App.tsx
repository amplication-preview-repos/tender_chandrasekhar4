import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { BuildingList } from "./building/BuildingList";
import { BuildingCreate } from "./building/BuildingCreate";
import { BuildingEdit } from "./building/BuildingEdit";
import { BuildingShow } from "./building/BuildingShow";
import { FoodItemList } from "./foodItem/FoodItemList";
import { FoodItemCreate } from "./foodItem/FoodItemCreate";
import { FoodItemEdit } from "./foodItem/FoodItemEdit";
import { FoodItemShow } from "./foodItem/FoodItemShow";
import { RequestList } from "./request/RequestList";
import { RequestCreate } from "./request/RequestCreate";
import { RequestEdit } from "./request/RequestEdit";
import { RequestShow } from "./request/RequestShow";
import { UserList } from "./user/UserList";
import { UserCreate } from "./user/UserCreate";
import { UserEdit } from "./user/UserEdit";
import { UserShow } from "./user/UserShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"FoodSharingService"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="Building"
          list={BuildingList}
          edit={BuildingEdit}
          create={BuildingCreate}
          show={BuildingShow}
        />
        <Resource
          name="FoodItem"
          list={FoodItemList}
          edit={FoodItemEdit}
          create={FoodItemCreate}
          show={FoodItemShow}
        />
        <Resource
          name="Request"
          list={RequestList}
          edit={RequestEdit}
          create={RequestCreate}
          show={RequestShow}
        />
        <Resource
          name="User"
          list={UserList}
          edit={UserEdit}
          create={UserCreate}
          show={UserShow}
        />
      </Admin>
    </div>
  );
};

export default App;
